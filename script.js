document.getElementById('heart-icon1').addEventListener('click', function() {
    const availableHeartsEl = document.getElementById('available-hearts');
    let availableHearts = parseInt(availableHeartsEl.innerText); 

    availableHearts += 1; 

    availableHeartsEl.innerText = availableHearts; 

});

